SELECT COUNT(*) AS total_fraud_txns
FROM transactions
WHERE label_fraud = 1;

SELECT 
    label_fraud,
    COUNT(*) AS txn_count
FROM transactions
GROUP BY label_fraud;

SELECT 
    channel,
    COUNT(*) AS total_txns,
    SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) AS fraud_txns,
    ROUND(
        100.0 * SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) / COUNT(*),
        2
    ) AS fraud_rate_pct
FROM transactions
GROUP BY channel
ORDER BY fraud_rate_pct DESC;

SELECT 
    txn_type,
    COUNT(*) AS total_txns,
    SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) AS fraud_txns,
    ROUND(
        100.0 * SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) / COUNT(*),
        2
    ) AS fraud_rate_pct
FROM transactions
GROUP BY txn_type
ORDER BY fraud_rate_pct DESC;

SELECT 
    t.account_id,
    COUNT(*) AS fraud_txns,
    SUM(t.amount) AS total_fraud_amount
FROM transactions t
WHERE t.label_fraud = 1
GROUP BY t.account_id
ORDER BY fraud_txns DESC, total_fraud_amount DESC
LIMIT 10;


SELECT 
    c.customer_id,
    c.full_name,
    COUNT(*) AS fraud_txns,
    SUM(t.amount) AS total_fraud_amount
FROM transactions t
JOIN accounts a   ON t.account_id = a.account_id
JOIN customers c  ON a.customer_id = c.customer_id
WHERE t.label_fraud = 1
GROUP BY c.customer_id, c.full_name
ORDER BY fraud_txns DESC, total_fraud_amount DESC
LIMIT 10;

SELECT 
    is_international,
    COUNT(*) AS total_txns,
    SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) AS fraud_txns,
    ROUND(
        100.0 * SUM(CASE WHEN label_fraud = 1 THEN 1 ELSE 0 END) / COUNT(*),
        2
    ) AS fraud_rate_pct
FROM transactions
GROUP BY is_international;


SELECT 
    fa.alert_id,
    fa.model_score,
    fa.alert_time,
    fa.alert_reason,
    fa.status,
    t.transaction_id,
    t.amount,
    t.txn_type,
    t.channel,
    t.is_international,
    c.customer_id,
    c.full_name
FROM fraud_alerts fa
JOIN transactions t ON fa.transaction_id = t.transaction_id
JOIN accounts a     ON t.account_id = a.account_id
JOIN customers c    ON a.customer_id = c.customer_id
WHERE fa.status IN ('open','under_review')
ORDER BY fa.model_score DESC, fa.alert_time DESC;

