use banking_fraud_detection ;


-- CUSTOMERS
CREATE TABLE customers (
    customer_id   INT PRIMARY KEY,
    full_name     VARCHAR(100),
    dob           DATE,
    gender        CHAR(1),
    city          VARCHAR(50),
    state         VARCHAR(50),
    created_at    DATETIME
);

-- ACCOUNTS
CREATE TABLE accounts (
    account_id    INT PRIMARY KEY,
    customer_id   INT,
    account_type  VARCHAR(20),
    opened_date   DATE,
    status        VARCHAR(20),
    balance       DECIMAL(15,2),
    CONSTRAINT fk_accounts_customer
      FOREIGN KEY (customer_id) REFERENCES customers(customer_id)
);

-- TRANSACTIONS
CREATE TABLE transactions (
    transaction_id   INT PRIMARY KEY,
    account_id       INT,
    txn_timestamp    DATETIME,
    amount           DECIMAL(15,2),
    txn_type         VARCHAR(20),   -- POS, ATM, ONLINE, UPI, NEFT etc.
    channel          VARCHAR(20),   -- card, atm, online, mobile
    is_international CHAR(1),       -- 'Y' / 'N'
    label_fraud      TINYINT,       -- 1 = fraud, 0 = genuine
    CONSTRAINT fk_txn_account
      FOREIGN KEY (account_id) REFERENCES accounts(account_id)
);

-- FRAUD ALERTS
CREATE TABLE fraud_alerts (
    alert_id       INT PRIMARY KEY,
    transaction_id INT,
    model_score    DECIMAL(5,2),   -- probability from ML model
    alert_time     DATETIME,
    alert_reason   VARCHAR(255),
    status         VARCHAR(20),    -- open/under_review/closed
    CONSTRAINT fk_alert_txn
      FOREIGN KEY (transaction_id) REFERENCES transactions(transaction_id)
);


